class Solution {
//		 åªèƒ½æ²¿ä¸€ä¸ªæ–¹å‘å‰è¿›
    public int numRookCaptures(char[][] board) {
		int x = 0, y = 0;
		for (int i = 0; i < 8; i++) {
			for (int j = 0; j < 8; j++) {
				if(board[i][j] == 'R'){
					x = j; y = i;break;
				}
			}
		}
		int cnt = 0;
		int[] dx = new int[]{1, 0, -1, 0};
		int[] dy = new int[]{0, 1, 0, -1};
		for (int i = 0; i <= 3; i++) {
			for (int step = 1;; step++) {
				int mx = x + dx[i] * step;
				int my = y + dy[i] * step;
				if(mx < 0 || mx >= 8 || my < 0 || my >= 8 || board[my][mx] == 'B')
					break;
				else if(board[my][mx] == 'p'){
					cnt++;
					break;
				}
			}
		}
		return cnt;
    }
}
